    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String correo = txtCorreo.getText();
        String contrase単a = String.valueOf(txtContrase単a.getText());

        //Validar si los campos estan vacios
        if (correo.isEmpty() || contrase単a.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor ingresa tu correo y contrase単a");
            return;
        }
        
        Connection con = ConexionSQL.ConexionSQLServer();

        //Validar que la conexion no sea nula
        if (con == null) {
            JOptionPane.showMessageDialog(this, "Error al conectar con la base de datos");
        }

        //Consulta sql para validar las credenciales
        if (con == null) {
            JOptionPane.showMessageDialog(this, "Error al conectar con la base de datos");
            return;
        }
        String query = "SELECT CORREO, CONTRA FROM USUARIO WHERE CORREO ="+"'"+txtCorreo.getText()+"'"+"AND CONTRA ="+"'"+txtContrase単a.getText()+"';";
        Statement st;
        ResultSet rs;
        String correoSQL = "";
        String contraSQL = "";
        
        try {
            st = con.createStatement();
            rs = st.executeQuery(query);
            
            while(rs.next()){
                correoSQL = rs.getString("correo");
                contraSQL = rs.getString("contra");
            }
            
            if(correoSQL.equals(txtCorreo.getText()) && (contraSQL.equals(txtContrase単a.getText()))){
                showMessageDialog(null,"Credenciales correctas, bienvenido");
                VentanaPrincipal vp = new VentanaPrincipal();
                vp.setVisible(true);
                this.dispose();
            }else{
                System.out.println("Credenciales incorrectas");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        //Consulta SQL para validar las credenciales
    }//GEN-LAST:event_jButton1MouseClicked
    
    
